int		ft_check_nb_tetri(char *str)
{
	int		i;
	int		nbt;

	i = 0;
	nbt = 0;
	while (str[i])
	{
		if (str[i] == '#')
			nbt++;
		i++;
	}
	return (nbt / 4);
}
