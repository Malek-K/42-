#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	s = (char *)malloc(size + 1);
	if (!s)
		return (NULL);
	i = 0;
	while (i <= size)
		s[i++] = '\0';
	return (s);
}
