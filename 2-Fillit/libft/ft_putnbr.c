#include "libft.h"

static void	ft_putintmin(void)
{
	ft_putstr("-2147483648");
}

void		ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putintmin();
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
		}
		ft_putchar(n % 10 + '0');
	}
}
