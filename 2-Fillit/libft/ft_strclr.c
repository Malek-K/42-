#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int size;

	i = 0;
	if (s)
	{
		size = ft_strlen(s);
		while (i <= size)
		{
			s[i] = '\0';
			i++;
		}
	}
}
