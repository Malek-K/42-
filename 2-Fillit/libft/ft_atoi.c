#include "libft.h"

int		ft_atoi(const char *np)
{
	int i;
	int nb;
	int neg;

	i = 0;
	nb = 0;
	neg = 1;
	while (np[i] == ' ' || np[i] == '\t' || np[i] == '\n' || np[i] == '\r' ||
			np[i] == '\v' || np[i] == '\f')
		i++;
	if (np[i] == '-')
		neg = -1;
	if (np[i] == '+' || np[i] == '-')
		i++;
	while (np[i] <= '9' && '0' <= np[i])
		nb = nb * 10 + np[i++] - '0';
	return (nb * neg);
}
