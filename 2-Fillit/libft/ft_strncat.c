#include "libft.h"

char	*ft_strncat(char *dest, const char *src, int n)
{
	int i;
	int j;

	j = ft_strlen(dest);
	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[j + i] = src[i];
		i++;
	}
	dest[j + i] = '\0';
	return (dest);
}
