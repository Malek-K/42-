#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		len;
	int		i;
	char	*cpy;

	i = 0;
	len = 0;
	while (src[len])
		len++;
	cpy = (char*)malloc(sizeof(*cpy) * (len + 1));
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
