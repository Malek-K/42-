int		ft_iterative_factorial(int nb)
{
	int		i;
	int		j;

	i = 0;
	j = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (i < nb)
		{
			i++;
			j = j * i;
		}
		return (j);
	}
	else
		return (0);
}
