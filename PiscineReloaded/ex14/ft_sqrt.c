int		ft_sqrt(int nb)
{
	int		a;

	a = 1;
	if (nb <= 0)
		return (0);
	while (a <= nb / a)
	{
		if (a == nb / a && nb % a == 0)
			return (a);
		a++;
	}
	return (0);
}
