#include "libft.h"

size_t		ft_wlen(unsigned int n)
{
	if (n <= 0x7F)
		return (1);
	else if (n <= 0x7FF)
		return (2);
	else if (n <= 0xFFFF)
		return (3);
	else if (n <= 0x10000)
		return (4);
	return (0);
}
