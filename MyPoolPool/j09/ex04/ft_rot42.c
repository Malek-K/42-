char	*ft_rot42(char *str)
{
	int		i;
	int		deb;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 122))
		{
			if (str[i] >= 65 && str[i] <= 90)
			{
				deb = 65;
				str[i] = (str[i] + deb - 42) % 26 + deb;
			}
			if (str[i] >= 97 && str[i] <= 122)
			{
				deb = 97;
				str[i] = (str[i] + deb - 2) % 26 + deb;
			}
		}
		i++;
	}
	return (str);
}
