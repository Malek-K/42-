int		ft_is_prime(int nb)
{
	int		i;

	i = 2;
	if (nb < 2)
		return (0);
	else
	{
		while (i < nb && (nb % i) != 0)
			i++;
		if (i == nb)
			return (1);
		else
			return (0);
	}
	return (1);
}
