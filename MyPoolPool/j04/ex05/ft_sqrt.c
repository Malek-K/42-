int		ft_sqrt(int nb)
{
	int		x;

	x = 1;
	if (nb <= 0)
		return (0);
	while (x <= nb / x)
	{
		if (x == nb / x && nb % x == 0)
			return (x);
		x++;
	}
	return (0);
}
